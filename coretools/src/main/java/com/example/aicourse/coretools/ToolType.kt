package com.example.aicourse.coretools

/**
 * Категории инструментов, доступных в системе.
 * Используется LLM для понимания природы инструмента и приложением для выбора стратегии обработки.
 */
enum class ToolType {
    /** Внешние сервисы, работающие по протоколу Model Context Protocol */
    MCP,
    /** Поиск по локальным или удаленным векторным базам знаний */
    RAG,
    /** Прямые вызовы сторонних REST/JSON API */
    HTTP_API,
    /** Внутренние команды приложения (например, "включи фонарик" или "смени тему") */
    SYSTEM
}